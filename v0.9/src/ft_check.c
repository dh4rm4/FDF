#include "../header/libfdf.h"

void	ft_check(char *str)
{
	int	i;

	i = -1;
	while (str[++i])
	{
		if (str[i] != ' ' && str[i] != '\n' && ft_isdigit(str[i]) == 0)
			ft_exit(0);
	}
}
